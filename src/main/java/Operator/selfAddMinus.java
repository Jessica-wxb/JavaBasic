package Operator;

public class selfAddMinus {
    public static void main(String[] args) {
        // 定义一个变量；
        int a = 3;
        // 自增运算
        int b = ++a;
        int c = 3;
        // 自减运算
        int d = --c;
        System.out.println("进行自增运算后的值等于" + b);
        System.out.println("进行自减运算后的值等于" + d);
    }
}
